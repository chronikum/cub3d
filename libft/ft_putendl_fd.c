#include "../inc/libft.h"

int	ft_putendl_fd(char *s, int fd)
{
	size_t	i;

	i = 0;
	if (s != NULL)
	{
		while (s[i] != '\0')
		{
			write(fd, s + i, 1);
			i++;
		}
		write(fd, "\n", 1);
	}
	return (1);
}
