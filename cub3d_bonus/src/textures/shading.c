#include "../../inc/cub3d.h"

/**
 * As further away, as darker the color is
 */
int	distance_color(int c, double d)
{
	if (d <= 1.)
		return (c);
	return (((int)(((16711680 & c) >> 16) / d) << 16)
		+ ((int)(((65280 & c) >> 8) / d) << 8)
		+ ((int)((255 & c) / d)));
}
